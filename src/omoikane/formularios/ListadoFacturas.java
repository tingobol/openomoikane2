    private void txtBusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            this.txtBusqueda.selectAll();
        }
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_DOWN) {
            int sigFila = tblFacturas.getSelectedRow()+1;
            if(sigFila < tblFacturas.getRowCount()) {
                this.tblFacturas.setRowSelectionInterval(sigFila, sigFila);
                this.tblFacturas.scrollRectToVisible(tblFacturas.getCellRect(sigFila, 1, true));
            }
        }
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_UP) {
            int antFila = tblFacturas.getSelectedRow()-1;
            if(antFila >= 0) {
                this.tblFacturas.setRowSelectionInterval(antFila, antFila);
                this.tblFacturas.scrollRectToVisible(tblFacturas.getCellRect(antFila, 1, true));
            }
        }
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_PAGE_DOWN) {
            int nFilas  = (int) this.jScrollPane1.getViewportBorderBounds().getHeight() / tblFacturas.getRowHeight();
            int sigFila = tblFacturas.getSelectedRow()+nFilas;
            if(sigFila > tblFacturas.getRowCount()) {
                sigFila = tblFacturas.getRowCount()-1;
            }
            if(sigFila < tblFacturas.getRowCount()) {
                this.tblFacturas.setRowSelectionInterval(sigFila, sigFila);
                this.tblFacturas.scrollRectToVisible(tblFacturas.getCellRect(sigFila, 1, true));
            }
        }
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_PAGE_UP) {
            int nFilas  = (int) this.jScrollPane1.getViewportBorderBounds().getHeight() / tblFacturas.getRowHeight();
            int antFila = tblFacturas.getSelectedRow()-nFilas;
            if(antFila < 0) {
                antFila = 0;
            }
            this.tblFacturas.setRowSelectionInterval(antFila, antFila);
            this.tblFacturas.scrollRectToVisible(tblFacturas.getCellRect(antFila, 1, true));
        }
        /*
        if(evt.getKeyCode() == java.awt.event.KeyEvent.VK_ESCAPE) {
            btnCerrar.doClick();
        }
        */
}//GEN-LAST:event_txtBusquedaKeyPressed

